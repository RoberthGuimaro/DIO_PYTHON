""" Objetivo Geral

Os processos de ETL, do inglês Extract, Transform, and Load (Extrair,
Tranfosmar e Carregar), envolvem ferramentas de software, voltadas para
a extração de dados de diversos sistemas e, também, a transformação desses
dados conforme regras de negócios bem definidas, e por fim, o carregamento
dos dados para um Data Mart e/ou Data Warehouse
"""

""" Conteúdo

Neste curso, serão abordados conceitos introdutórios e práticos derecionados
para extração, transformação e carregamento de dados, tópicos que são obrigatórios
para os processos de ETL. Os conceitos aplicados são baseados na linguagem Python
em conjunto com a biblioteca Pandas, onde adicionalmente será aplicado também o
framework Luigi, poissibilitando a visualização de dados tratados e centralizados
em um dashboard que pode aprentar a integração da informação de forma inteligente.
"""