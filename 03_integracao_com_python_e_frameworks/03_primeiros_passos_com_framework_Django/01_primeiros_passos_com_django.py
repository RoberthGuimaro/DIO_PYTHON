# Mas o que é Django?

# o que é Django? Django é um framework web Python de alto nível
# que permite o rápido dsenvolvimento de sites seguros e de fácil
# manutenção.

# Django é um framework de web server-side extremamente popular e
# repleto de características, escrito em Python. O módulo mostra
# por que o Django é um dos frameworks web mais populares, como
# configurar um ambiente de desenvolvimento e como começar a usa-lo
# para criar seus próprios aplicativos da Web.

# Django é um framework web Python de alto nível que permite o rápido
# desenvolvimento de sites seguros e de fácil manutenção. Construido
# por desenvolvedores experientes, o Django cuida de grande parte do
# trabalho de desenvolvimento web, para que você possa se concentrar
# em escrever seu aplicativo sem precisar reinventar a roda.

# É gratuito e de código aberto, tem uma comunidade próspera e ativa,
# ótima deocumentação e muitas opções de suporte gratuito e pago.

# Completo
# Django segue a filosofia de "Baterias incluídas" e fornece quase
# tudo que desenvolvedores possam querer fazer "fora da caixa". Como
# tudo o que você precisa é parte de um "produto", tudo funciona perfeitamente
# junto, seguindo princípios de design de projetos.

# Versátil
# Django pode ser (e tem sido) utilizado para construir quase todo tipo de
# website, passando por redes sociais e sites de notícias. Ele pode trabalhar
# com qualquer framework do lado do cliente, e pode entregar conteúdo em
# praticamente qualquer format (incluindo HTML, feeds RSS, JSON, XML, etc.)

# Seguro
# Django ajuda os desenvolvedores a evitar os erros de segurança mais comuns,
# fornecendo um framework que foi desenhado para "fazer as coisas certas", de
# modo a proteger o website automaticamente.

# Escalável
# Django usa uma arquitetura baseada em componentes "shared-nothing"
# ("nada-compartilhado"), cada parte da arquitetura é independente das
# outras, e consequentemente podem ser substituídas ou mudadas caso
# necessário.

# Sustentável
# O código do Django é escrito usando princípios de design e padrões
# que encorajam a criação de código sustentável (que facilita a manutenção)
# e reutilizável.

# Portável
# Django é escrito em Python, que executa emmuitas plataformas. Isso
# significa que você não está preso em nenhuma plataforma de servidor em
# particular, e pode executar seus aplicativos em mutias distribuições do
# linux, Windows e Mac OS X. Além disso, o Django tem um bom suporte em
# muitos provedores de servidores de WEB.

# De onde veio?
# Django foi inicialmente desenvolvido entre 2003 e 2005 por um time de web
# que era responsável por criar e manter sites de jornal. Depois de criar um
# número de sites, o time começou a fatorar e reutilizar muitos de seus códigos
# comuns e padrões de design. Esse código comum evoluiu para um framework genérico
# de desenvolvimento WEB.
