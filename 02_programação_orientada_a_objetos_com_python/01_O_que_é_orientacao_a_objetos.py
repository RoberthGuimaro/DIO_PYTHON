# Programação orientada a Objetos
#
#   O paradigma de programação orientada a objetos
#   estrutura o código astraindo problemas em objetos
#   do mundo real, facilitando o entendimento do código
#   e tornando-o mais modular e extensível. os dois
#   conceitos chaves para aprender POO são:
#   classes e objetos.

