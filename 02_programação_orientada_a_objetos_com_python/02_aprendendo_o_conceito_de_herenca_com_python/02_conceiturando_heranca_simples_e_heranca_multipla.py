# Herança Simples

# Quando uma clase filha herda apenas uma
# classe pai, ela é chamada de herança simples.

## Exemplo

class A:
    pass

class B(A):
    pass

### Herança múltipla

### Quando uma classe filha herda de várias
### classes pai, ela é chamada de herança
### múltipla.

#### Exemplo

class A:
    pass

class B:
    pass

class C(A, B):
    pass