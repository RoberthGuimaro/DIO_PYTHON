## O que sao interfaces?

## Importante!
## Interfaces denifem o que uma classe
## deve fazer e nao como.

### Python tem interface?

### O conceito de interface é definir um
### contrato, onde são declarados os métodos
### (o que deve ser feito) e suas respectivas
### assinaturas. Em python utilizamos classes
### abstratas para criar contratos. Classes
### abstratas não podem ser instanciadas.

